    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
        // Ambil ID pelanggan dari field teks
        String id_pelanggan = txtPelanggan.getText();
        
        // Pastikan ID pelanggan tidak kosong
        if (id_pelanggan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Pelanggan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Ambil data baru dari field teks
        String nama_pelanggan = txtNapel.getText();
        String gender = txtGender.getText();
        String alamat = txtAlamat.getText();

        // Query untuk mengupdate data pelanggan berdasarkan ID
        String sqlUpdate = "UPDATE pelanggan SET nama_pelanggan = ?, gender = ?, alamat = ? WHERE id_pelanggan = ?";
        PreparedStatement perintahUpdate = KoneksiDatabase.prepareStatement(sqlUpdate);
        perintahUpdate.setString(1, nama_pelanggan);
        perintahUpdate.setString(2, gender);
        perintahUpdate.setString(3, alamat);
        perintahUpdate.setString(4, id_pelanggan);

        int affectedRows = perintahUpdate.executeUpdate(); // Eksekusi query

        // Cek apakah ada baris yang terpengaruh
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "ID Pelanggan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Tutup PreparedStatement
        perintahUpdate.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saat mengupdate data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       try {
        // Ambil ID pelanggan dari field teks
        String id_pelanggan = txtPelanggan.getText();
        
        // Pastikan ID pelanggan tidak kosong
        if (id_pelanggan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Pelanggan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Query untuk menghapus data pelanggan berdasarkan ID
        String sqlHapus = "DELETE FROM pelanggan WHERE id_pelanggan = ?";
        PreparedStatement perintahHapus = KoneksiDatabase.prepareStatement(sqlHapus);
        perintahHapus.setString(1, id_pelanggan);
        
        int affectedRows = perintahHapus.executeUpdate(); // Eksekusi query

        // Cek apakah ada baris yang terpengaruh
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "ID Pelanggan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Tutup PreparedStatement
        perintahHapus.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

