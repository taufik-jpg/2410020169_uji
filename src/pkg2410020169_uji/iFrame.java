    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            // Ambil ID Iklan dari field teks
            String id_iklan = txtDklan.getText();

            // Pastikan ID Iklan tidak kosong
            if (id_iklan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID Iklan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Query untuk menghapus data Iklan berdasarkan ID
            String sqlHapus = "DELETE FROM iklan WHERE id_iklan = ?";
            PreparedStatement perintahHapus = KoneksiDatabase.prepareStatement(sqlHapus);
            perintahHapus.setString(1, id_iklan);

            int affectedRows = perintahHapus.executeUpdate(); // Eksekusi query

            // Cek apakah ada baris yang terpengaruh
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "ID Iklan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Tutup PreparedStatement
            perintahHapus.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            // Ambil ID Iklan dari field teks
            String id_iklan = txtDklan.getText();

            // Pastikan ID Iklan tidak kosong
            if (id_iklan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID Iklan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Ambil data baru dari field teks
            String judul = txtJudul.getText();
            String tayang = txtTayang.getText();
            String waktu = txtWaktu.getText();
            String harga = txtHarga.getText();

            // Query untuk mengupdate data Iklan berdasarkan ID
            String sqlUpdate = "UPDATE iklan SET judul = ?, tayang = ?, waktu = ?, harga = ? WHERE id_iklan = ?";
            PreparedStatement perintahUpdate = KoneksiDatabase.prepareStatement(sqlUpdate);
            perintahUpdate.setString(1, judul);
            perintahUpdate.setString(2, tayang);
            perintahUpdate.setString(3, waktu);
            perintahUpdate.setString(4, harga);
            perintahUpdate.setString(5, id_iklan);

            int affectedRows = perintahUpdate.executeUpdate(); // Eksekusi query

            // Cek apakah ada baris yang terpengaruh
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "ID Iklan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Tutup PreparedStatement
            perintahUpdate.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat mengupdate data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

